“无 key 项”就是在 table 构造器里只写了值，没有显式写 key 的项。
比如：
local t = { "a", "b", "c" }
这里 "a"、"b"、"c" 都是无 key 项。
Lua 会自动给它们分配数字 key：
local t = {
    [1] = "a",
    [2] = "b",
    [3] = "c"
}
所以这两个写法等价：
local t = { "a", "b", "c" }
local t = {
    [1] = "a",
    [2] = "b",
    [3] = "c"
}
回到你的代码：
arr = {[0]=0, 1, [2]="abc", 2, nil, 3, 4, 5, nil, [100]=100, 6}
其中：
[0] = 0
[2] = "abc"
[100] = 100
这些是有 key 项，因为你明确写了 key。
而这些：
1
2
nil
3
4
5
nil
6
都是无 key 项，因为它们只是值，没有写 key。
Lua 会给无 key 项自动编号，从 1 开始数：
第 1 个无 key 项：1      -> arr[1] = 1
第 2 个无 key 项：2      -> arr[2] = 2
第 3 个无 key 项：nil    -> arr[3] = nil
第 4 个无 key 项：3      -> arr[4] = 3
第 5 个无 key 项：4      -> arr[5] = 4
第 6 个无 key 项：5      -> arr[6] = 5
第 7 个无 key 项：nil    -> arr[7] = nil
第 8 个无 key 项：6      -> arr[8] = 6
重点来了：
[2] = "abc"
先设置了：
arr[2] = "abc"
但是后面这个无 key 项：
2
它是第 2 个无 key 项，所以 Lua 自动给它 key 2：
arr[2] = 2
于是同一个 key 被赋值了两次：
arr[2] = "abc"
arr[2] = 2
后面的覆盖前面的，所以 "abc" 被覆盖掉了。
可以理解成你的代码大致等价于：
arr = {}

arr[0] = 0
arr[1] = 1
arr[2] = "abc"
arr[2] = 2      -- 覆盖上面的 "abc"
arr[3] = nil
arr[4] = 3
arr[5] = 4
arr[6] = 5
arr[7] = nil
arr[100] = 100
arr[8] = 6
最后 arr[2] 只剩下：
2
不是：
"abc"
一句话：
无 key 项会被 Lua 自动分配从 1 开始的整数 key；你的第二个无 key 项刚好也是 key 2，所以覆盖了前面显式写的 [2] = "abc"。
