你的代码：
arr = {[0]=0,1,[2]="abc",2,nil,3,4,5,nil,[100]=100,6}
Lua 的 table 构造规则大概是：
显式写了 [key] = value，就用你指定的 key
没有写 key 的值，会自动分配连续整数 key
自动整数 key 从 1 开始
显式 key 不会改变自动 key 的计数
